//W.A.J.P to get the character at the given index within the String. Original String = 
//Tops Technologies! The character at position 0 is T, The character at position 10 is
//o

package assignment;

public class indexstring {
	public static void main(String[] args) {
		
	String os = "Tops Technologies!";
	
	char charAtIndex0 = os.charAt(0);
	System.out.println("The character at position 0 is " + charAtIndex0);
	
	char charAtIndex10 = os.charAt(10);
	System.out.println("The character at position 10 is " + charAtIndex10);
	
	}
}
